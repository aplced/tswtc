package TSWTalismans;

public class GlyphXMLS
{
	private String name;
	private int evadeRating;
	private int blockRating;
	private int critRating;
	private int critPower;
	private int penetrationRating;
	private int hitRating;

	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getEvadeRating() {
		return evadeRating;
	}
	public void setEvadeRating(int evadeRating) {
		this.evadeRating = evadeRating;
	}
	public int getBlockRating() {
		return blockRating;
	}
	public void setBlockRating(int blockRating) {
		this.blockRating = blockRating;
	}
	public int getCritRating() {
		return critRating;
	}
	public void setCritRating(int critRating) {
		this.critRating = critRating;
	}
	public int getCritPower() {
		return critPower;
	}
	public void setCritPower(int critPower) {
		this.critPower = critPower;
	}
	public int getPenetrationRating() {
		return penetrationRating;
	}
	public void setPenetrationRating(int penetrationRating) {
		this.penetrationRating = penetrationRating;
	}
	public int getHitRating() {
		return hitRating;
	}
	public void setHitRating(int hitRating) {
		this.hitRating = hitRating;
	}
}
